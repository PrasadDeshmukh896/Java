package Operators;

public class ArithmaticOperation {
    public static void main(String[] args) {
        int a=30;
        int b=40;

        System.out.println("Addition Operator "+(a+b));//+ Operator
        System.out.println("Subtraction Operator "+(b-a));//  - Operator
        System.out.println("Multiplication Operator "+(a*b));//  * Operator
        System.out.println("Division Operator "+(b/a));//  / Operator
        System.out.println("Modulo Operator "+(b%a));//  % Operator
    }
}
