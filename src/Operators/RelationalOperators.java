package Operators;

public class RelationalOperators {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        //Realations Operators always Returns boolean Type Of Value

        System.out.println("Check For  Equality "+(a==b));
        System.out.println("Negation And Equality "+(a!=b));
        System.out.println("Less Than "+(a<b));
        System.out.println("Greter Than "+(a>b));
        System.out.println("Greter Than And Equals "+ (a>=b));
        System.out.println("Less Than And Equals "+ (a<=b));
    }
}
